const AreaToInterior areaTransitions[] = 
{
    //DungeonNW Entrance
    {0,0,0,0,16,1},  
    //Town1 Entrance
    {0,0,2,2,41,2}, 

};

const size_t areaTransitionsCount = sizeof(areaTransitions)/sizeof(areaTransitions[0]);

uint8_t LookupInteriorID(uint8_t zx, uint8_t zy, uint8_t ax, uint8_t ay, uint16_t gid) // Determine which map to switch to when entering an Overworld->Interior transition tile
{
    for (size_t i = 0; i < areaTransitionsCount; i++) 
    {
        const AreaToInterior& t = areaTransitions[i];
        if (t.zoneX == zx && t.zoneY == zy && t.areaX == ax && t.areaY == ay && t.entranceGID == gid) return t.interiorID;
    }
    return 0; // 0 = no interior
}