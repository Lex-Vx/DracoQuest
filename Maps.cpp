// Registry storage 

static const MapInfo* areaRegistry[6][6][5][5] = { nullptr };
static const MapInfo* interiorRegistry[256] = { nullptr };

// Default fallback map
const MapInfo default_map_info = { nullptr, 0, 0 };

// Registration functions

void RegisterArea(uint8_t zx, uint8_t zy, uint8_t ax, uint8_t ay, const MapInfo* map) 
{
    if (zx < 6 && zy < 6 && ax < 5 && ay < 5)
    {
        areaRegistry[zx][zy][ax][ay] = map;
    }  
}

const MapInfo* GetAreaMap(uint8_t zx, uint8_t zy, uint8_t ax, uint8_t ay) 
{
    if (zx < 6 && zy < 6 && ax < 5 && ay < 5) 
    {
        const MapInfo* m = areaRegistry[zx][zy][ax][ay];
        if (m != nullptr) return m;
    }
    return &default_map_info;
}

// Interior maps
void RegisterInterior(uint8_t id, const MapInfo* map) 
{
    interiorRegistry[id] = map;
}

const MapInfo* GetInterior(uint8_t id) 
{
    if (interiorRegistry[id] != nullptr)
    {
        return interiorRegistry[id];
    }
    return &default_map_info;
}

